_GREETINGS_BY_TYPE: dict[str, dict[str, list[str]]] = {
    "birthday": _BIRTHDAY_GREETINGS,
    "anniversary": _ANNIVERSARY_GREETINGS,
    "other": _OTHER_GREETINGS,
}


# ── Public API ───────────────────────────────────────────────────────
def generate_greeting(name: str, event_type: str) -> str:
    """Return a random personalized greeting for the given event."""
    rel = _detect_relationship(name)
    pool = _GREETINGS_BY_TYPE.get(event_type, _OTHER_GREETINGS)

    templates = pool.get(rel) or pool["generic"]
    text = random.choice(templates)
    return text.replace("{name}", name)
