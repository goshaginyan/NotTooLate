"""Generate personalized greetings based on event type and person name/relationship."""

import random

# ── Relationship detection ───────────────────────────────────────────
# Maps keyword fragments (lowercased) to a relationship category.
_RELATIONSHIP_KEYWORDS: dict[str, str] = {
    "мам": "mom",
    "мат": "mom",
    "пап": "dad",
    "отец": "dad",
    "отц": "dad",
    "жен": "wife",
    "супруг": "wife",       # супруга
    "муж": "husband",
    "сын": "son",
    "дочь": "daughter",
    "дочк": "daughter",
    "дочер": "daughter",
    "бабушк": "grandma",
    "бабул": "grandma",
    "дедушк": "grandpa",
    "дедул": "grandpa",
    "сестр": "sister",
    "брат": "brother",
    "друг": "friend",
    "друзь": "friend",
    "подруг": "friend",
    "колле": "colleague",
    "начальн": "boss",
    "шеф": "boss",
}


def _detect_relationship(name: str) -> str:
    """Return relationship key or 'generic'."""
    low = name.lower()
    for keyword, rel in _RELATIONSHIP_KEYWORDS.items():
        if keyword in low:
            return rel
    return "generic"


